#include <iostream>

using namespace std;

int main()
{
	const int GREATE_VICTORY_SALSU = 612;
	// 상수 선언 , 612로 초기화
	const int GREATE_VICTORY_GWIJU = 1019;
	// 상수 선언 , 1019로 초기

	cout << "고구려 살수대첩 연도 : " << GREATE_VICTORY_SALSU << "년" << endl;
	// 문자열 출력한 후, 상수의 값을 출력하고, "년"문자열을 출력함
	cout << "고구려 귀주대첩 연도 : " << GREATE_VICTORY_GWIJU << "년" << endl;
	// 문자열 출력한 후, 상수의 값을 출력하고, "년"문자열을 출력함
	return 0;
}
